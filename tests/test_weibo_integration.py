class TestDebugTokens:
    def test_debug_tokens_file_valid(self):
        tokens = _require_tokens()
        assert tokens["platform"] == "weibo"
        assert tokens["cookies"].get("SUB"), "Missing SUB cookie"
        assert tokens["expires_at"] > tokens["obtained_at"], "expires_at should be after obtained_at"
        assert tokens["cookies"].get("SUBP"), "Missing SUBP cookie"
