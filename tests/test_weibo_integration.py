# ── Real API: Comments ───────────────────────────────────────


class TestCommentsAPI:
    @pytest.mark.asyncio
    async def test_fetch_comment_highlights(self):
        """Fetch real comment highlights for a known post."""
        tokens = _require_tokens()
        cookie = _cookie_str(tokens)

        # First get a post ID from mobile API
        posts = await fetch_user_posts_mobile(cookie, user_id=TEST_USER_ID, max_posts=1)
        if not posts:
            pytest.skip("No posts available to test comments")

        post_id = str(posts[0]["id"])
        config = _require_config()

        highlights = await fetch_weibo_comment_highlights(post_id, config)

        assert isinstance(highlights, list)
        if highlights:
            for h in highlights:
                assert h.content, "Comment has empty content"
                assert h.like_count >= 0, "Comment has negative like_count"
        await close_session()

    @pytest.mark.asyncio
    async def test_parse_real_comments(self):
        """Parse real comment data through _parse_comment."""
        tokens = _require_tokens()
        cookie = _cookie_str(tokens)

        # Get a post ID
        posts = await fetch_user_posts_mobile(cookie, user_id=TEST_USER_ID, max_posts=1)
        if not posts:
            pytest.skip("No posts available")

        post_id = str(posts[0]["id"])
        config = _require_config()
        highlights = await fetch_weibo_comment_highlights(post_id, config)

        # Highlight objects should already be parsed and valid
        assert all(isinstance(h.content, str) for h in highlights)
        assert all(isinstance(h.like_count, int) for h in highlights)
        await close_session()


